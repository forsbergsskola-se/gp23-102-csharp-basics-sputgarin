
// P09_06Negation

// Prompt the user for a number.
Console.WriteLine("Please type a number: ");

// Reading the user input and converting it to an Int32 then assigning it to the variable x.
int x = Convert.ToInt32(Console.ReadLine());

// Negating the variable x and assigning it to the variable y.
int y = -x;

// Print the result.
Console.WriteLine("The negation is: {0}",y);

